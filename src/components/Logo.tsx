interface LogoProps {
  variant?: "full" | "extended" | "icon";
  className?: string;
  href?: string;
}

/**
 * Craft Logo Component
 * - Use variant="full" for "craft" wordmark (default)
 * - Use variant="extended" for "craft.fast" full name
 * - Use variant="icon" for chat pages (shows just the "C" icon)
 */
export default function Logo({
  variant = "full",
  className = "",
  href = "/",
}: LogoProps) {
  const logoContent =
    variant === "icon" ? (
      <LogoIcon className={className} />
    ) : variant === "extended" ? (
      <LogoExtended className={className} />
    ) : (
      <LogoFull className={className} />
    );

  // If href is provided, wrap in Link
  if (href) {
    return (
      <Link
        href={href}
        className="cursor-pointer hover:opacity-80 transition-opacity inline-block"
      >
        {logoContent}
      </Link>
    );
  }

  return logoContent;
}
