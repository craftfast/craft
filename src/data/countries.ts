// Helper function to get tax ID info for a country
export function getTaxIdInfo(countryCode: string) {
    return TAX_ID_INFO[countryCode] || TAX_ID_INFO.DEFAULT;
}

// Helper function to get country name by code
export function getCountryName(countryCode: string): string | undefined {
    return COUNTRIES.find((c) => c.code === countryCode)?.name;
}

// Helper function to get states for a country
export function getStatesForCountry(countryCode: string) {
    return STATES_BY_COUNTRY[countryCode] || null;
}

// Helper function to check if country has states
export function countryHasStates(countryCode: string): boolean {
    return countryCode in STATES_BY_COUNTRY;
}
